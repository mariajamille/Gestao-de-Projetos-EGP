                        <h3 class="Subtopico fw-bolder">Sistemas de Monitoramento e Avaliação</h3>
                        <!-- subtopico -->

                        <p>Pode ser entendido, em sentido lato, como o conjunto de atividades – articuladas,
                            sistemáticas e formalizadas – de produção, registro, acompanhamento e análise crítica de
                            informações geradas na gestão de políticas públicas, de seus programas, produtos e serviços,
                            por meio das organizações, agentes e públicos-alvo envolvidos, com a finalidade de subsidiar
                            a tomada decisão quanto aos esforços necessários para aprimoramento da ação pública.</p>
                        <p>Trata-se, pois, de um conjunto de atividades inerentes ao ciclo de gerenciamento da produção
                            das políticas públicas, voltadas à sistematização da informação acerca dos aspectos
                            considerados críticos para sucesso dos programas.</p>
