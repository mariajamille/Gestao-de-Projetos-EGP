                        <div class="alert alert-success" role="alert">
                            <p style="text-align: center;" id="text-alert-verde"><strong>Avaliação</strong> </p>
                        </div>
                        <p>Trata-se de um conjunto de atividades no qual se desenvolve a ação de coleta, análise e
                            interpretação de dados e informações para fazer um julgamento objetivo de um projeto
                            concluído ou de uma fase do projeto (TENÓRIO, 1995) e tem o propósito de subsidiar os
                            gestores com informações mais aprofundadas e detalhadas sobre o funcionamento e os efeitos
                            do programa, levantadas nas pesquisas de avaliação.</p>
                        <p>Para isso, precisa-se de perguntas formuladas de forma empírica ou não sobre o diagnóstico,
                            implantação, execução, resultados e impactos do programa, com fins de subsidiar o seu
                            aperfeiçoamento. O momento e o timing adequados para realização das avaliações dependem da
                            etapa do ciclo da política pública a ser avaliada.</p>
                        <p>É desejável que os resultados gerados pelas pesquisas de avaliação sejam de fácil
                            interpretação para o gestor, tendo em vista que as pesquisas de avaliação podem utilizar-se
                            de metodologias sofisticadas para a coleta e a análise das informações.</p>

