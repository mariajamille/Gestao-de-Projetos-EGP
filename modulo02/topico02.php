                            Sistemas e Painéis de Monitoramento</h3>
                        <!-- subtopico -->

                        <p>Um sistema ou painel de monitoramento deve estar definido segundo seus objetivos e
                            utilidades. A seleção de indicadores é parte crucial da construção de um painel, lembrando
                            que a função de um indicador está normalmente atrelada à representação de um fenômeno
                            pontual e específico, enquanto a função de um painel de monitoramento é a de representar uma
                            dinâmica relacional entre indicadores, ou seja, o seu foco é a representação de relações
                            causais, processuais e/ou de complementaridade dos fenômenos medidos pelos indicadores.</p>
                        <p>Veja o esquema a seguir que nos ajuda a visualizar dimensões do projeto a partir de
                            questionamentos que necessitam ter respostas claras e compreendidas por todos os envolvidos
                            nos processos, nas etapas, nas atividades e ações que o projeto propõe.</p>
