                                    class="fa fa-bullseye CorVerdeEstado"></i> Monitorar os riscos do projeto e a
                                adequada aplicação das contingências;</li>
                            <li class=" wow fadeInLeft" data-wow-delay="1.3s"><i
                                    class="fa fa-bullseye CorVerdeEstado"></i> Propiciar informações precisas sobre
