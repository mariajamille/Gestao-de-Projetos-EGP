                            complexidade, a equipe do projeto dispenderá mais ou menos esforço em seu gerenciamento. O
                            gerente do projeto, com a colaboração de sua equipe, é o responsável pela determinação do
                            grau adequado de rigor a ser usado em cada processo de gerenciamento, para um projeto
                            específico, atendendo ao mínimo previsto na Política de Gerenciamento de Projetos da
                            Organização.</p>
