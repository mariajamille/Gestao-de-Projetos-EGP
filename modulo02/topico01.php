                        <h3 class="Subtopico fw-bolder" id="item01.3">01.3 - Monitoramento e Controle</h3>
                        <!-- subtopico -->

                        <p>O processo de monitoramento e controle é realizado para acompanhar a execução do projeto, de
                            forma que possíveis problemas possam ser identificados tempestivamente e ações corretivas
                            sejam providenciadas.</p>
                        <p> Vale destacar que as fases de execução e monitoramento ocorrem concomitantemente. Durante a
                            execução, o gerente do projeto realiza as entregas dos produtos (intermediários e finais),
                            momento em que é avaliada a qualidade da entrega pela unidade demandante.</p>
                        <p>Nos casos de não conformidade, a unidade demandante pode solicitar alterações no projeto a
                            fim de assegurar que o resultado final atinja o objetivo proposto. As solicitações de
                            mudanças também podem ocorrer por outros motivos, como a identificação de alterações ou
                            impedimentos legais ou processuais, mudanças de diretrizes superiores, dentre outros.</p>
                        <p>O processo de monitoramento e controle se complementa com as atividades de comunicação com as
                            partes interessadas.</p>
                        <br>

