                        <p class="scrool">Existe uma diferença entre <strong>metodologia de desenvolvimento</strong> de
                            um produto ou
                            serviço e a <strong>metodologia para o gerenciamento do projeto</strong> que vai realizar
                            esse desenvolvimento. A primeira trata do ciclo de vida para gerar o produto ou serviço
                            (escopo do produto), jáa segunda é responsável por iniciar, planejar, executar, monitorar,
                            controlar e encerrar esse projeto, levando em consideração o escopo (do projeto), em
                            conjunto com outras variáveis como tempo, custo, risco etc.</p>
                        <p class="scrool">Uma metodologia é composta por técnicas e processos que visam aumentar e
                            garantir a
                            eficiência do trabalho realizado dentro de uma organização. Em Gerenciamento de Projetos, a
                            utilização de uma metodologia é considerada fator crítico de sucesso.</p>
