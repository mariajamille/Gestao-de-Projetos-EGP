                        <h3 class="Subtopico fw-bolder scrool" id="item01.2">01.2 - Alguns aspectos da Metodologia de
                            Gerenciamento de Projetos no PMBOK</h3>
                        <!-- subtopico -->

                        <p class="scrool">Na última década, duas formas de gerenciamento de projetos se destacaram, e
                            até hoje são
                            amplamente utilizadas nas organizações. A primeira se baseia nas metodologias ágeis, que
                            busca valorizar a interação e a colaboração entre os indivíduos. Já a segunda forma, é
                            baseada no Guia PMBOK, que fornece um conjunto de práticas e processos de gerenciamento de
                            projetos nas mais diversas áreas de conhecimento, desde o gerenciamento de escopo até o
                            gerenciamento das aquisições relacionadas a este.
                        </p><br>
                        <p class="scrool">De acordo com o PMBOK, gerenciamento de projetos é a aplicação de
                            conhecimentos, habilidades,
                            ferramentas e técnicas nas atividades do projeto, a fim de atender seus requisitos. Cada
                            fase do projeto em si é planejada, executada e concluída. O PMBOK também fornece e promove
                            um conjunto de vocabulários comum à profissão de gestão de projetos, que é considerado um
                            padrão e um elemento essencial a esta. O PMI considera essa norma como uma referência para o
                            gerenciamento de projetos, mas ressalta que serve apenas de guia e não como uma metodologia
                            em si.</p><br>
                        <p class="scrool">Um dos mais importantes princípios da administração de projetos é o
                            planejamento de todas as
                            fases, sempre com maior precisão no início. É importante ressaltar, que devido à integração
                            dos processos ao longo do projeto, todas as fases das áreas de conhecimento (escopo, tempo e
                            custo) precisam ser minuciosamente administradas.</p><br>
                        <p class="scrool">O Guia PMBOK é amplamente reconhecido e contém diversas práticas que são
                            aplicáveis à maioria
                            dos processos. Para os profissionais da área, o guia é um dos possíveis consensos em
                            gerenciamento de projetos, principalmente, em razão das práticas que possui.</p>
