                        <h3 class="Subtopico fw-bolder scrool">Gestão de Projetos no Setor Público</h3>
                        <!-- subtítulo -->
                        <br>
                        <p class="scrool">Advoga-se que as reformas administrativas correspondem ao principal motivo
                            para o incremento
                            da nova gestão pública, propiciando uma nova concepção da administração pública,
                            incorporando conceitos, ferramentas e práticas inovadoras que tiveram sucesso na área
                            privada, encarando a visão de cidadão como cliente e utilizando estratégias voltadas não
                            mais para os processos em si, mas para o resultado (BRESSER-PEREIRA, 1998).</p><br>
                        <p class="scrool">Nesse cenário, a ideia de controle é vista como elemento essencial para tomada
                            de decisões da
                            alta gestão, promovendo mais eficiência na alocação dos recursos, com vistas a ofertar
                            serviços públicos de qualidade e a infraestrutura necessária para o desenvolvimento
                            econômico e social.</p><br>
                        <p class="scrool">O grande volume de recursos que os gestores públicos têm a responsabilidade de
                            administrar
                            necessita de projetos bem elaborados para que a execução seja correta e a finalidade seja
                            atendida. A elaboração, o acompanhamento e o controle na execução dos projetos são de
                            fundamental importância para uma boa e fiel gestão nos recursos públicos.</p><br>
                        <p class="scrool">Dessa forma, um maior profissionalismo na gestão de projetos públicos é vital
                            para o alcance
                            de resultados, ou seja, para a prestação de um serviço de melhor qualidade para os cidadãos.
                        </p><br>
                        <p class="scrool">Ciente disso, o Governo do Estado do Ceará decidiu investir na capacitação de
                            seus
                            profissionais e nomeou um grupo técnico de trabalho para desenvolver uma Metodologia de
                            Gerenciamento de Projetos para o Estado, bem como para propor políticas, diretrizes, plano
                            de capacitação e ferramentas e estratégias de implantação. Essa Metodologia foi baseada nas
                            boas práticas do PMBOK – A Guide to the Project Management Body of Knowledge, que é um guia
                            que aborda a somatória de conhecimentos e as melhores práticas dentro da área de gerência de
                            projetos, além de ser um material genérico, que pode ser usado para qualquer tipo de
                            projeto, e de ser o mais difundido mundialmente (SEPLAG, 2008).</p><br>
                        <p class="scrool">A partir de 2007, foi desenvolvida uma ferramenta de gestão de Monitoramento e
                            Acompanhamento
                            de Projetos Prioritários – MAPP. O MAPP tem como objetivo facilitar o processo decisório
                            frente à eleição dos projetos estratégicos, especialmente da carteira de investimentos, de
                            forma que todos os órgãos e entidades do Poder Executivo passaram a utilizar esta ferramenta
                            para submeter um projeto que considera prioritário para o cumprimento das estratégias do
                            Governo.</p><br>
                        <p class="scrool">A partir disso, a Secretaria do Planejamento e Gestão – SEPLAG redefiniu o
                            Sistema Estadual
                            de Planejamento e instituiu a Rede Estadual de Planejamento, demandando que cada
                            Órgão/Entidade implantasse, por meio de sua Unidade Setorial de Planejamento, o escritório
                            de projetos, conforme disposto no Decreto Estadual nº 29.917, de 08 de outubro de 2009.</p>
