                        <br>
                        <p><strong>Esses estágios são:</strong></p>
                        <br>

                        <!--Alerta -->

                        <div class="alert alert-info" role="alert">
                            <p><strong>Iniciação </strong></p> <br>

                            <p>Essa etapa apresenta os processos necessários à definição de um novo projeto ou de uma
                                nova fase através da obtenção de uma autorização para iniciar o projeto ou fase. Nesse
                                primeiro momento, devemos identificar a real necessidade do projeto para a organização,
                                sua viabilidade, orçamentos e cronogramas em um nível ainda macro.</p>
                        </div>

                        <!--Fim Alerta -->
                        <br>
                        <div class="alert alert-danger" role="alert">
                            <p><strong>Planejamento</strong></p>
                            <p>Esse estágio descreve os processos necessários para definir o escopo, refinar os
                                objetivos e definir a linha de ação necessária para alcançar os objetivos para os quais
                                o projeto foi criado. Principais características:</p><br>
                            <div>
                                <ul class="none text-white">
                                    <li><i class="fa fa-bullseye CinzaEstado"></i> Construção progressiva da
                                        estruturação das tarefas no tempo e suas interdependências junto a aspectos
                                        financeiros;</li>
                                    <li><i class="fa fa-bullseye CinzaEstado"></i> Análise de impactos gerados por
                                        riscos, mudanças e ajustes;</li>
                                    <li><i class="fa fa-bullseye CinzaEstado"></i> Definição e aperfeiçoamento do
                                        escopo, do custo e do agendamento das atividades, através da coleta de
                                        informações.</li>
                                </ul>

                            </div>

                        </div>

                        <!--Fim Alerta -->
                        <br>
                        <div class="alert alert-success" role="alert">
                            <p><strong>Execução</strong></p>
                            <p>Essa fase apresenta os processos necessários para executar o trabalho definido no Plano
                                de Gerenciamento do Projeto, a fim de satisfazer as especificações do projeto.
                                Principais características:</p><br>
                            <div>
                                <ul class="none" style="color: #44833d;">
                                    <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Reunião de pessoas e de
                                        recursos, para concretizar as ideias, manifestando-se através de ação;</li>
                                    <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Constituição de processos que
                                        visam terminar o trabalho definido no plano de gerenciamento do projeto, e
                                        envolve a coordenação de pessoas e de recursos sejam eles humanos, materiais ou
                                        financeiros;</li>
                                    <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Abordagem do escopo e
                                        implementação mudança.</li>
                                </ul>
                            </div>
                        </div>

                        <!--Fim Alerta -->
                        <br>
                        <div class="alert alert-primary" role="alert">
                            <p><strong>Monitoramento</strong></p>
                            <p>Estágio em que os processos são exibidos para analisar, acompanhar, e controlar o
                                progresso e desempenho do projeto, identificando quaisquer áreas nas quais possa ser
                                necessário realizar alterações no plano, iniciando, assim, as mudanças necessárias.
                                Principais características:</p><br>
                            <div>
                                <ul class="none text-white">
                                    <li><i class="fa fa-bullseye AzulEstadoEscuro"></i> Identificação de variações
                                        relacionadas ao planejamento, podendo-se atuar corretivamente na execução ou se
                                        adequar eventos futuros;</li>
                                    <li><i class="fa fa-bullseye AzulEstadoEscuro"></i> Observação da execução no
                                        intuito de medir o desempenho, recomendar ações, antecipar problemas, e
                                        controlar mudanças através de monitoramento contínuo;</li>
                                    <li><i class="fa fa-bullseye AzulEstadoClaro"></i> Várias fases podem ser aplicadas,
                                        a fim de implementar ações, quando as variações comprometerem os objetivos.</li>
                                </ul>
                            </div>
                        </div>

                        <!--Fim Alerta -->

                        <!--Fim Alerta -->
                        <br>
                        <div class="alert alert-success" role="alert">
                            <p><strong>Conclusão</strong></p>
                            <p>Momento em que são vistos os processos necessários para finalizar todas as atividades de
                                todos os grupos de processos, objetivando encerrar, formalmente, o projeto ou a fase.
                                Principais características:</p><br>
                            <div>
                                <ul class="none" style="color: #44833d;">
                                    <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Aceite do resultado ou
                                        produto;</li>
                                    <li><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Processos para finalizar
                                        todas as atividades e, segundo PMBOK®, encerramento de projetos e encerramento
                                        de aquisições.</li>
                                </ul>
                            </div>

                        </div>
                        <br>
                        <p>A imagem 09 mostra a forma como os processos interagem entre si e aponta o nível de
                            sobreposição em várias situações. Caso o processo esteja dividido em fases, os grupos de
                            processos se inter-relacionam dentro de cada fase (PMBOK, 2015).</p>
