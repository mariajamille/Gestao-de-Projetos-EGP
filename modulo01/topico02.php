                        <h3 class="Subtopico fw-bolder scrool">Conceito de Projeto</h3>
                        <!-- subtítulo -->

                        <p class="scrool">A palavra <strong>PROJETO</strong> é utilizada por pessoas comuns e por
                            profissionais de
                            diversas áreas do conhecimento para expressar o produto de uma atividade intelectual de
                            interesse particular de indivíduos ou de uma organização relacionada ao alcance de um
                            objetivo idealizado.</p>
                        <p class="scrool">Essa palavra é utilizada em diferentes ambientes da vida empresarial, muitas
                            vezes
                            representando coisas diferentes. Diversos são os conceitos de projetos. Para alguns
                            especialistas, o projeto se constitui como algo que seja utilizado para analisar uma dada
                            alternativa, outros interpretam como algo mais aprofundado e de longa duração nas
                            organizações. Alguns entendem projeto não apenas como a etapa de planejamento, enquanto
                            outros demonstram interesse pelo projeto no que se refere às projeções, independentemente de
                            estarem tratando deste nas organizações privadas ou públicas.</p>
                        <p class="scrool fw-semibold"> Destacamos, a seguir, alguns conceitos de projeto:</p>
                        <br>

                        <!-- CITAÇÃO -->
                        <div class="bd-callout bd-callout-success">
                            <p class="scrool">
                                "Conjunto de ações executadas, de forma coordenada, por uma organização transitória, à
                                qual são alocados insumos sob a forma de recursos (humanos, financeiros, materiais etc.)
                                e serviços (gerenciamento, compras, transportes etc.) para, em um dado prazo, alcançar
                                um objetivo determinado."
                            <p class="text-end">(VALERIANO, 1998, p.19) –</p>
                            </p>
                        </div>
                        <br>
                        <!-- CITAÇÃO -->
                        <div class="bd-callout bd-callout-success">
                            <p class="scrool">
                                "Trata-se de empreendimento com objetivo bem definido, que consome recursos e opera sob
                                pressões de prazos, custos e qualidade."
                            <p class="text-end">(KERZNER, 2006, p.15) –</p>
                            </p>
                        </div>
                        <br>
                        <!-- CITAÇÃO -->
                        <div class="bd-callout bd-callout-success">
                            <p class="scrool">
                                "É um empreendimento temporário com o objetivo de criar um produto ou serviço único."
                            <p class="text-end">(HELDMAN, 2006, p.5) –</p>
