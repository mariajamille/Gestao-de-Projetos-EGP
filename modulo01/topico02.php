                        <h3 class="Subtopico fw-bolder scrool" id="item02.2">02.2 - Diferença entre Projeto e Programas
                        </h3>
                        <!-- subtítulo -->

                        <p class="scrool">Podemos, nesse momento, utilizar os conceitos definidos pelo <em><a
                                    href="https://www.pmi.org/" target="_blank">Project Management Institute</a></em>, o
                            qual considera que:</p>
                        <br>
                        <!-- CITAÇÃO -->
                        <div class="bd-callout bd-callout-success">
                            <p class="scrool">
                                "Um projeto é um esforço temporário empreendido para criar um produto, serviço ou
                                resultado único. A natureza temporária dos projetos indica que eles têm um início e um
                                término definidos. O término é alcançado quando os objetivos do projeto são atingidos ou
                                quando o projeto é encerrado porque os seus objetivos não serão ou não podem ser
                                alcançados, ou quando a necessidade do projeto deixar de existir."
                            <p class="text-end">(PMBOK, 2013, p. 3) –</p>
                            </p>
                        </div>
                        <br>
                        <!-- CITAÇÃO -->
                        <div class="bd-callout bd-callout-success">
                            <p class="scrool">
                                "Um programa é um grupo de projetos relacionados gerenciados de modo coordenado, a fim
                                de obter benefícios e controle não disponíveis se gerenciados individualmente. Os
                                programas podem incluir elementos relacionados de trabalho, que estão fora do escopo dos
                                projetos individuais do programa. Os programas são constituídos de vários componentes. A
                                maioria desses componentes são os projetos separados do programa, mas outro componente é
                                o esforço de gerenciamento e infraestrutura necessário para gerenciar o programa."
                            <p class="text-end">(PMBOK, 2012, p. 5) –</p>
                            </p>
                        </div>
